import torch
from src.python.models.graph_embedding_generation import GraphEmbeddingGeneration
from src.python.models.mapzero_policy_net import MapZeroPolicyNet
from src.python.models.value_net import ValueNet

class MapZero(torch.nn.Module):
    def __init__(self,dim_feat_dfg, dim_feat_cgra,out_dim, n_heads, len_action_space, negative_slope = 0.02):
        super().__init__()
        self.graph_embed_generation = GraphEmbeddingGeneration(dim_feat_dfg,dim_feat_cgra,out_dim,negative_slope,n_heads)
        self.value_net = ValueNet(out_dim,out_dim,negative_slope)
        self.policy_net = MapZeroPolicyNet(out_dim,out_dim,len_action_space,negative_slope)
    def get_critic(self):
        return self.value_net
    def get_actor(self):
        return self.policy_net
    def forward(self, dfg_features, dfg_edge_indices: torch.Tensor, cgra_features, cgra_edge_indices: torch.Tensor, vertex_to_be_mapped_feature,
                    mask, dfg_pad_mask, cgra_pad_mask):
        
        state_vector = self.graph_embed_generation(dfg_features,
                                                    dfg_edge_indices,
                                                    cgra_features, 
                                                    cgra_edge_indices,
                                                    vertex_to_be_mapped_feature,
                                                    dfg_pad_mask,
                                                    cgra_pad_mask)
        p = self.policy_net(state_vector)
        v = self.value_net(state_vector)
        # don't change
        p = torch.where(mask == 0,-torch.inf, p)
        return p,v

    
